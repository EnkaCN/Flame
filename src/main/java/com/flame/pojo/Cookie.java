package com.flame.pojo;

public class Cookie {

    private Integer cookieId;

    private String cookieName;

    private Integer cookieUser;

    private Boolean cookieSuper;

    public Integer getCookieId() {
        return cookieId;
    }

    public void setCookieId(Integer cookieId) {
        this.cookieId = cookieId;
    }

    public String getCookieName() {
        return cookieName;
    }

    public void setCookieName(String cookieName) {
        this.cookieName = cookieName;
    }

    public Integer getCookieUser() {
        return cookieUser;
    }

    public void setCookieUser(Integer cookieUser) {
        this.cookieUser = cookieUser;
    }

    public Boolean getCookieSuper() {
        return cookieSuper;
    }

    public void setCookieSuper(Boolean cookieSuper) {
        this.cookieSuper = cookieSuper;
    }
}
